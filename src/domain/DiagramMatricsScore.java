public class DiagramMatricsScore {
	private int diagramId;
	private int matricsId;
	private double score;
	/**
	 * @return the diagramId
	 */
	public int getDiagramId() {
		return diagramId;
	}
	/**
	 * @param diagramId the diagramId to set
	 */
	public void setDiagramId(int diagramId) {
		this.diagramId = diagramId;
	}
	/**
	 * @return the matricsId
	 */
	public int getMatricsId() {
		return matricsId;
	}
	/**
	 * @param matricsId the matricsId to set
	 */
	public void setMatricsId(int matricsId) {
		this.matricsId = matricsId;
	}
	/**
	 * @return the score
	 */
	public double getScore() {
		return score;
	}
	/**
	 * @param score the score to set
	 */
	public void setScore(double score) {
		this.score = score;
	}
	
	
}
